package com.digital_menu_card_ingredients;

public class Veggies {
	
	private boolean onions;
	private boolean tomatoes;
	private boolean capsicums;
	private boolean jalapenos;
	private boolean blackOlives;
	private boolean sweetcorn;
	private boolean mushrooms;
	private boolean panner;
	private boolean redPaprika;
	private boolean babyCorn;
	private boolean pasta;
	
	
	public boolean isOnions() {
		return onions;
	}
	public void setOnions(boolean onions) {
		this.onions = onions;
	}
	public boolean isTomatoes() {
		return tomatoes;
	}
	public void setTomatoes(boolean tomatoes) {
		this.tomatoes = tomatoes;
	}
	public boolean isCapsicums() {
		return capsicums;
	}
	public void setCapsicums(boolean capsicums) {
		this.capsicums = capsicums;
	}
	public boolean isJalapenos() {
		return jalapenos;
	}
	public void setJalapenos(boolean jalapenos) {
		this.jalapenos = jalapenos;
	}
	public boolean isBlackOlives() {
		return blackOlives;
	}
	public void setBlackOlives(boolean blackOlives) {
		this.blackOlives = blackOlives;
	}
	public boolean isSweetcorn() {
		return sweetcorn;
	}
	public void setSweetcorn(boolean sweetcorn) {
		this.sweetcorn = sweetcorn;
	}
	public boolean isMushrooms() {
		return mushrooms;
	}
	public void setMushrooms(boolean mushrooms) {
		this.mushrooms = mushrooms;
	}
	public boolean isPanner() {
		return panner;
	}
	public void setPanner(boolean panner) {
		this.panner = panner;
	}
	public boolean isRedPaprika() {
		return redPaprika;
	}
	public void setRedPaprika(boolean redPaprika) {
		this.redPaprika = redPaprika;
	}
	public boolean isBabyCorn() {
		return babyCorn;
	}
	public void setBabyCorn(boolean babyCorn) {
		this.babyCorn = babyCorn;
	}
	public boolean isPasta() {
		return pasta;
	}
	public void setPasta(boolean pasta) {
		this.pasta = pasta;
	}
	
	
	
}
